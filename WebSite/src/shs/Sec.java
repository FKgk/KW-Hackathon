package shs;

public class Sec {
	
	private String shsSecName;
	private String shsUserID;
	
	private String secName;
	private String secDaysTime;
	private String secCoCode;
	private String secUserName;
	public String getShsSecName() {
		return shsSecName;
	}
	public void setShsSecName(String shsSecName) {
		this.shsSecName = shsSecName;
	}
	public String getShsUserID() {
		return shsUserID;
	}
	public void setShsUserID(String shsUserID) {
		this.shsUserID = shsUserID;
	}
	public String getSecName() {
		return secName;
	}
	public void setSecName(String secName) {
		this.secName = secName;
	}
	public String getSecDaysTime() {
		return secDaysTime;
	}
	public void setSecDaysTime(String secDaysTime) {
		this.secDaysTime = secDaysTime;
	}
	public String getSecCoCode() {
		return secCoCode;
	}
	public void setSecCoCode(String secCoCode) {
		this.secCoCode = secCoCode;
	}
	public String getSecUserName() {
		return secUserName;
	}
	public void setSecUserName(String secUserName) {
		this.secUserName = secUserName;
	}
	
}
